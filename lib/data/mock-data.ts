export function generateServicesForCropCycle(
  cropType: CropType,
  plantingDate: string,
  hectares: number,
  farmerId: string,
  cropCycleId: string,
  corridorId: string,
  seasonId: string,
): Omit<ServiceEvent, "id" | "createdAt" | "updatedAt">[] {
  const template = SERVICE_TEMPLATES.find((t) => t.cropType === cropType)
  if (!template) return []

  const plantDate = new Date(plantingDate)

  return template.services.map((svc) => {
    const plannedStart = new Date(plantDate)
    plannedStart.setDate(plannedStart.getDate() + svc.daysFromPlanting)
    const plannedEnd = new Date(plannedStart)
    plannedEnd.setDate(plannedEnd.getDate() + svc.windowDays)

    return {
      cropCycleId,
      farmerId,
      corridorId,
      seasonId,
      serviceType: svc.serviceType,
      description: svc.description,
      plannedDateStart: plannedStart.toISOString().split("T")[0],
      plannedDateEnd: plannedEnd.toISOString().split("T")[0],
      status: "planned" as const,
      estimatedCost: svc.estimatedCostPerHectare * hectares,
      currency: "NGN" as const,
      slaWindowDays: svc.windowDays,
    }
  })
}

export function calculateFarmerSettlement(
  farmerId: string,
  cropCycleId: string,
  deliveredTons: number,
  pricePerTon: number,
  services: ServiceEvent[],
): Omit<FarmerSettlement, "id"> {
  // Corrected type annotation
  const grossValue = deliveredTons * pricePerTon
  const serviceBreakdown = services
    .filter((s) => s.actualCost)
    .map((s) => ({
      serviceType: s.serviceType,
      amount: s.actualCost!,
    }))
  const totalCosts = serviceBreakdown.reduce((sum, s) => sum + s.amount, 0)

  return {
    farmerId,
    corridorId: "corridor-kaduna-001",
    seasonId: "season-2024-wet",
    cropCycleId,
    grossHarvestValue: grossValue,
    deliveredVolumeTons: deliveredTons,
    pricePerTon,
    totalServiceCosts: totalCosts,
    serviceBreakdown,
    netFarmerPayout: grossValue - totalCosts,
    status: "calculated",
    calculatedAt: new Date().toISOString(),
  }
}
